#19. Programa que introduzca dos números y devuelva cuál de los dos es mayor, menor o son iguales
var1=int(input("Introduce un valor: "))
var2=int(input("Introduce otro valor: "))

if var1>var2:
    print("El primer valor es mas grande")
if var1<var2:
    print("El segundo valor es mas grande")
if var1==var2:
    print("Los dos valores son iguales")
